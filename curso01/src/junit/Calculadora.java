package junit;
/*
 * Classe que implementa cálculos
 *
 * @author Matheus Inacio
 * @version 0.1
 */
public class Calculadora {
	
	/**
	 * Faz a som de dois números
	 * @param x
	 * @param y
	 * @return soma de x+y
	 */
	public int soma(int x, int y) {
		return x+y;
	}
	
	/**
	 * Faz a divisão de x por y
	 * @param x
	 * @param y
	 * @return divisão de x/y
	 */
	public int divide(int x, int y) {
		return x/y;
	}

}
